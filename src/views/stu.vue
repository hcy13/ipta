(2)清華學院的美資班課程設計為多元，師資與課程來自各學院。

4.請問學士班是否大二後會做分流？

A：(1)本院學士班主要把「科技藝術」跨域當成專業領域來培育人才，因此不做分流。
(2)學生未來若志趣不合，可透過轉系方式轉至感興趣之學系就讀。

5.請問考進學士班未來修習之專業課程為何？

A：請參考藝術學院學士班「修課說明」說明。

6.請問學士班上課地點？

A：(1)目前學士班課程部分在校本部上課、部分在南大校區上課，但通識課程以及一些跨域課程會在校本部上課（每天有校本部與南大校區接駁車）。
(2)校本部即將興建嶄新的「藝術學院大樓」（全清華最美的大樓）與「教育學院大樓」於台積館旁紫荊葉雕塑作品的對面，藝術學院大樓後方也興建新的學生宿舍。

6.請問學士班學生住宿？

A：學生可選住南大校區或是校本部，未來請恰學務處。

7.請問學士班畢業後出路？

A：(1)學術升學：科技藝術研究所、新媒體研究所、傳播科技研究所，以及創新科技的理工研究所。
(2)就業方向：科技產品開發、智能生活、多媒體設計、互動科技、虛擬實境科技、新媒體傳媒、動畫與遊戲、廣告公關、影視科技、時尚流行、文化創意等各類產業工作。
(3)藝術創作：專職之科技藝術、新媒體藝術家。

